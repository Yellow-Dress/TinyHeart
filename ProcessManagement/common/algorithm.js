module.exports = {
	encryptUrl : function(id){
		var crypted = encrypt(key, iv, id);
		return crypted;
	},
	decryptUrl : function(crypted){
		var dec = decrypt(key, iv, crypted);
		console.log("数据解密后:", dec);
		return dec;
	}
}